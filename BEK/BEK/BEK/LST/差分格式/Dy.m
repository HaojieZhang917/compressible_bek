function [ LY1,LY2 ] = Dy( NY,kind )
% Derivative matrix in y-direction based on 10th central difference.
load('./baseflow.mat','y');
N=NY+1;  h=1.0/NY;
e = ones(N,1);
a1=-2/2520/h;  a2=25/2520/h;  a3=-150/2520/h;  a4=600/2520/h;  a5=-2100/2520/h;
b5=2/2520/h;  b4=-25/2520/h;  b3=150/2520/h;  b2=-600/2520/h;  b1=2100/2520/h;
L1 = spdiags([a1*e a2*e a3*e a4*e a5*e b1*e b2*e b3*e b4*e b5*e ], [-5,-4,-3,-2,-1,1,2,3,4,5], N, N);
L1=full(L1);  L1(1:5,:)=0;  L1(N-4:N,:)=0;
L1(1,1)=-25/12/h;  L1(1,2)=48/12/h;  L1(1,3)=-36/12/h;  L1(1,4)=16/12/h;  L1(1,5)=-3/12/h;
L1(2,1)=-3/12/h;  L1(2,2)=-10/12/h;  L1(2,3)=18/12/h;  L1(2,4)=-6/12/h;  L1(2,5)=1/12/h;
L1(3,1)=1/12/h;  L1(3,2)=-8/12/h;  L1(3,4)=8/12/h;  L1(3,5)=-1/12/h;
L1(4,1)=-1/60/h;  L1(4,2)=9/60/h;  L1(4,3)=-45/60/h;  L1(4,5)=45/60/h;  L1(4,6)=-9/60/h;  L1(4,7)=1/60/h;
c1=-3.571428571428e-3/h;  c2=3.80952380952e-2/h;  c3=-0.2/h;  c4=0.8/h;
L1(5,1)=-c1;  L1(5,2)=-c2;  L1(5,3)=-c3;  L1(5,4)=-c4;   L1(5,6)=c4;  L1(5,7)=c3;  L1(5,8)=c2;  L1(5,9)=c1;
L1(N,N)=25/12/h;  L1(N,N-1)=-48/12/h;  L1(N,N-2)=36/12/h;  L1(N,N-3)=-16/12/h;  L1(N,N-4)=3/12/h;
L1(N-1,N)=3/12/h;  L1(N-1,N-1)=10/12/h;  L1(N-1,N-2)=-18/12/h;  L1(N-1,N-3)=6/12/h;  L1(N-1,N-4)=-1/12/h;
L1(N-2,N)=-1/12/h;  L1(N-2,N-1)=8/12/h;  L1(N-2,N-3)=-8/12/h;  L1(N-2,N-4)=1/12/h;
L1(N-3,N)=1/60/h;  L1(N-3,N-1)=-9/60/h;  L1(N-3,N-2)=45/60/h;  L1(N-3,N-4)=-45/60/h;  L1(N-3,N-5)=9/60/h;  L1(N-3,N-6)=-1/60/h;
L1(N-4,N)=c1;  L1(N-4,N-1)=c2;  L1(N-4,N-2)=c3;  L1(N-4,N-3)=c4;
L1(N-4,N-5)=-c4;  L1(N-4,N-6)=-c3;  L1(N-4,N-7)=-c2;  L1(N-4,N-8)=-c1;

if(kind==0)
    LY1=L1;  LY2=0;
end
if(kind==1 || kind==2)
    L2=L1*L1;  sy=L1*y;  sy=1./sy;  syy=L1*sy;
    for k=1:NY+1
        L2(k,:)=L2(k,:)*sy(k)*sy(k)+L1(k,:)*sy(k)*syy(k);
        L1(k,:)=L1(k,:)*sy(k);
    end
end
if(kind==1)
    LY1=full(L1);  LY2=full(L2);
end
if(kind==2)
    L=zeros(5*N,5*N);  LL=zeros(5*N,5*N);
    for i=1:N
        m1=1+5*(i-1);
        for j=1:N
            m2=1+5*(j-1);
            L(m1,m2)=L1(i,j);  L(m1+1,m2+1)=L1(i,j);  L(m1+2,m2+2)=L1(i,j);  L(m1+3,m2+3)=L1(i,j);  L(m1+4,m2+4)=L1(i,j);
            LL(m1,m2)=L2(i,j);  LL(m1+1,m2+1)=L2(i,j);  LL(m1+2,m2+2)=L2(i,j);  LL(m1+3,m2+3)=L2(i,j);  LL(m1+4,m2+4)=L2(i,j);
        end
    end
    LY1=sparse(L);  LY2=sparse(LL);
end




